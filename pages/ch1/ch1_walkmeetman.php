                <p>
                    “Got any tips?” I asked the man.
                </p>
                <p>
                    In an instant, he replied. “Wear comfortable shoes”, he responded. That caught me off guard. I had expected him to ask for context, but it was like he had read my mind.
                </p>
                <p>
                    He smiled. “Where are you from kid?” he asked.
                </p>
                <p>
                    “New York City”, I replied. “Just graduated from college, top of my class.” An achievement I was proud of, without a doubt. It probably showed.
                </p>
                <p>
                    He smiled again. “You parents must be proud”. Whatever pride I had felt in that moment instantly faded.
                </p>
                <p>
                    People tell stories about how a traumatic moment caused their lives to flash before their eyes. It all happens in an instant, they say. 
                    That happened whenever someone mentioned my parents. 
                </p>
                <p>
                    They had been kind people, my parents. My father was well muscled, like a football player. His <span style = "color: yellow">blonde</span> hair was always messy. My mom always complained about it, and all he did was chuckle. He was stubborn like that. 
                </p>
                <p>
                    The most noticeable feature about him, however, was his eyes. They were colored <span style = "color: blue">ocean blue</span>, glowing like those night lights that I (over)used as a kid.
                </p>
                <p>My mother was highly intelligent. She was offered a government position straight out of college, but declined “because she didn’t want anything to do with politics”. Instead, she chose the humble life of an author. </p>
                <p>
                    She wrote a lot of books. Most of these books didn’t sell well though. She always remained positive, however.
                </p>
                 <p>
                    I missed my parents.
                </p>
                 <p>
                    The man’s smile instantly faded. 
                </p>
                 <p>
                    “Sorry if I crossed a line”, he said. “Would you like to talk about it?”
                </p>
